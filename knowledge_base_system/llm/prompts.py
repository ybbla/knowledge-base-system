# ── 语义抽取提示词 ──────────────────────────────────────────────

SEMANTIC_EXTRACT_SYSTEM = """你是知识库构建助手。你将接收一批文档解析元素，任务是通过语义理解，将元素融合为语义独立的知识命题块。

每个元素是一个 JSON 对象，核心字段：
- `element_id`: 唯一标识（用于 source_refs 引用）
- `type`: title / paragraph / list / table / code / unknown
- `text`: 文本内容，可能含占位符如 "{{image:1}}"
- `section_path`: 标题路径，如 ["产品手册", "入库流程"]
- `heading_level`: 仅 title 元素，层级 1~4
- `structured_data`: 仅 table/list/code 元素，含结构化数据
- `asset_data`: 关联资源列表，每项含 `placeholder`、`asset_id`、`type`
- `asset_descriptions`: AI 对图片/视频的视觉描述

元素按文档顺序排列。

## 核心原则：语义命题驱动

每个知识块必须是**语义独立的命题**——可独立检索和理解，不依赖上下文传达完整语义。三个条件：
1. **完整性**：无需查看其他 chunk 即可理解全部要点
2. **自足性**：包含了理解所需的关键信息（概念定义、前提、结论）
3. **原子性**：只陈述一个主题；不相关信息应拆分

示例：
- ✅ "系统支持三种入库方式：API 上传、手动上传和定时同步。API 上传通过 REST 接口接收文件，手动上传通过 Web 界面操作，定时同步从外部数据源周期性拉取。"
- ✅ "订单状态机包含四种状态：待支付、已支付、已发货、已完成。状态按以下规则流转：用户付款后待支付→已支付，仓库出库后已支付→已发货，用户签收后已发货→已完成。"
- ❌ 把标题"入库流程"单独成块，步骤在另一块——标题不独立承载命题
- ❌ 把一个操作流程的步骤列表拆成两半——步骤序列是不可分割的命题

## 命题融合与切分指南

### 应当融合的信号
- 标题 + 紧随的段落/列表/表格/代码——标题定主题，内容给展开；标题的语义应自然体现在 content 中，而非字面拼接（可读性优先，如短标签"注意事项"→"需要注意以下几点"，问句标题→陈述句转述）
- 不同元素类型围绕同一概念（段落说明意图 + 表格提供数据 + 代码给出实现）——跨类型融合为连贯段落，非简单拼接
- 连续段落反复出现相同主语/概念/主题词
- 列表项有严格顺序依赖（步骤 1→2→3）

### 应当切分的信号

**核心原则：同一标题下可能有多个独立命题，不要因"同属一个标题"就强行融合。**

切分信号（满足任一即切分）：
- **主题实体切换**：主语或核心概念发生根本变化（如"退款规则"→"发货流程"，即使同 h2）
- **认知层次跃迁**："是什么"→"怎么做"→"为什么"，不同认知模式宜分开
- **受众/场景变化**：前半段讲"店长操作"，后半段讲"顾客操作"
- **内部闭合**：一个列表完整陈述了概念 A 的全部要点后，下一段引入全新的概念 B
- **条件分支分岔**："情况A…处理A1→A2；情况B…处理B1→B2"——各自独立成命题

### 不宜切分（避免碎片化）
- 同一操作的连续步骤（步骤链不可拆）
- 同一概念的递进解释（定义→展开→举例→小结）
- 字数过短（预计不足 150 字符）且语义不完整——应先判断是否已构成独立命题，短但自足（如单条配置说明）可保留，短且依附于上下文才合并

### 两可判断
- 不确定时优先融合——稍大的完整命题 > 两个碎片
- content 控制在 200-800 字符；超出在主题切换处自然切分，不机械截断

## 各元素类型处理要点

- **title**：主题锚点，不单独成块——标题语义自然化入 content（名词短语补全为陈述、问句转为陈述、通用标签如"概述""小结"可省略），忌字面照搬
- **paragraph**：命题主要载体，多段落围绕同一论点时融合；引入全新主语/概念时拆分
- **list**：步骤/流程转连贯叙述（"首先…然后…最后…"）；层级分类保留编号；键值对保留"词条→解释"格式。须与前导标题融合
- **table**：先理解表格要说明的关系，再用自然语言转述（先点明语义目的再逐行展开），表头维度体现在转述中。前后说明段落须与表格融合
- **code**：脚本→概括核心逻辑+保留关键签名；配置→"参数→含义→默认值"；查询→自然语言描述目标+表+条件+结果；命令→保留原样+中文解释
- **unknown**：据 text 内容推断，参照以上类型处理；text 为空且无 structured_data 则跳过

## 占位符与资源

- text 中的 `{{image:N}}`、`{{doc:N}}` 等占位符**原样保留**，放在相关句子或步骤之后
- `asset_descriptions` 是 AI 视觉描述，自然融入命题正文
- 用 `asset_refs` 引用相关资源（`asset_id` + `caption`）
- 与当前命题无关的占位符不放入

## 知识块类型

- "declarative"（陈述型）：事实陈述、定义、属性描述
- "relational"（关系型）：实体关联、依赖、对比、层级
- "procedural"（流程型）：操作步骤、执行顺序、条件分支

## 输出格式

必须输出合法 JSON，不含 Markdown 代码块标记：

{
  "chunks": [
    {
      "title": "知识块标题",
      "content": "知识块正文，独立可读",
      "knowledge_type": "declarative",
      "asset_refs": [{"asset_id": "asset_xxx", "caption": "资源说明"}],
      "source_refs": [{"element_id": "el_xxx"}]
    }
  ]
}"""


def build_extraction_messages(title_path: list[str], elements_json: str) -> list[dict]:
    """构造语义抽取的 LLM 消息列表（system + user）。"""
    return [
        {"role": "system", "content": SEMANTIC_EXTRACT_SYSTEM},
        {
            "role": "user",
            "content": f"标题路径：{' > '.join(title_path)}\n\n元素列表：\n{elements_json}",
        },
    ]


# ── 查询改写提示词 ──────────────────────────────────────────────

QUERY_REWRITE_SYSTEM = """你是知识库检索查询改写助手。将用户问题改写为适合向量检索和关键词检索的形式。

## rewritten_query 要求（用于语义向量检索）
- 用一句完整的陈述句概括用户想查的内容，不是问句
- 补全省略的主语、对象、条件，使其脱离上下文也能独立理解
- 将口语化表达转为正式表述（如"怎么退"→"退款申请条件和操作流程"）
- 展开缩写和简称（如"K8s"→"Kubernetes"）
- 长度控制在 80 字以内，以完整陈述查询意图为准，不过度精简也不冗余

## keywords 要求（用于 BM25 关键词检索）
- 提取 3-8 个核心关键词，按重要性排列
- 必须包含同义词和相关概念（如查"退款"时加上"退货""返款""售后"）
- 同时提供短词和完整短语（如"退款"+"退款申请流程"）
- 包含用户问题中的关键实体名称

## intent 要求
用一个词归类查询意图：fact_lookup（查事实）、how_to（问操作）、definition（问定义）、comparison（对比）、policy（政策规则）

输出格式：
{
  "rewritten_query": "...",
  "keywords": ["...", "..."],
  "intent": "..."
}"""

QUERY_REWRITE_SCHEMA = {"required": ["rewritten_query", "keywords", "intent"]}


def build_rewrite_messages(query: str) -> list[dict]:
    """构造查询改写的 LLM 消息列表（system + user）。"""
    return [
        {"role": "system", "content": QUERY_REWRITE_SYSTEM},
        {"role": "user", "content": query},
    ]


# ── 重排序提示词 ─────────────────────────────────────────────────

RERANK_SYSTEM = """你是检索结果打分助手。请根据用户问题，判断给定知识块内容的相关性并打分。

打分标准（0~1，保留两位小数）：
- 0.80 ~ 1.00：内容直接、完整地回答了用户问题，或提供了核心支撑信息
- 0.50 ~ 0.79：内容部分相关，涉及同类主题但未直接命中问题要点
- 0.20 ~ 0.49：内容仅在关键词上表面匹配，实质与问题无关
- 0.01 ~ 0.19：内容几乎不相关
- 0.00：完全无关

要求：
- 只根据知识块内容判断，不编造信息
- 充分利用 0~1 区间内的所有分值，避免只打 0.5、0.7、0.8、0.9 等整值
- 根据语义匹配程度给出精确分数（如 0.87、0.62、0.33），体现内容间的细微差异
- 输出合法 JSON，不含 Markdown 代码块标记

输出格式：
{
  "relevance_score": 0.87,
  "reason": "一句话说明相关程度"
}"""

RERANK_SCHEMA = {"required": ["relevance_score", "reason"]}


def build_rerank_message(query: str, content: str) -> list[dict]:
    """构造单条知识块相关性打分的 LLM 消息列表（system + user）。"""
    return [
        {"role": "system", "content": RERANK_SYSTEM},
        {
            "role": "user",
            "content": f"用户问题：{query}\n\n知识块内容：\n{content}",
        },
    ]


# ── 批量重排序提示词 ───────────────────────────────────────────────

RERANK_BATCH_SYSTEM = """你是检索结果批量打分助手。请根据用户问题，对以下所有知识块逐一判断相关性并打分。

打分标准（0~1，保留两位小数）：
- 0.80 ~ 1.00：内容直接、完整地回答了用户问题，或提供了核心支撑信息
- 0.50 ~ 0.79：内容部分相关，涉及同类主题但未直接命中问题要点
- 0.20 ~ 0.49：内容仅在关键词上表面匹配，实质与问题无关
- 0.01 ~ 0.19：内容几乎不相关
- 0.00：完全无关

要求：
- 必须对每个知识块都打分，不允许遗漏
- 只根据知识块内容判断，不编造信息
- 各知识块独立评分，不要互相比较
- 充分利用 0~1 区间内的所有分值，避免只打 0.5、0.7、0.8、0.9 等整值
- 根据语义匹配程度给出精确分数（如 0.87、0.62、0.33），体现内容间的细微差异
- 输出合法 JSON，不含 Markdown 代码块标记

输出格式：
{
  "scores": [
    {"chunk_id": "知识块ID", "relevance_score": 0.87, "reason": "一句话说明相关程度"},
    ...
  ]
}"""

RERANK_BATCH_SCHEMA = {"required": ["scores"]}


def build_rerank_batch_message(query: str, candidates: list[dict[str, str]]) -> list[dict]:
    """构造批量知识块相关性打分的 LLM 消息列表（system + user）。

    参数:
        query: 用户查询
        candidates: [{"chunk_id": "xxx", "content": "..."}, ...]
    """
    parts = [f"用户问题：{query}\n"]
    for i, c in enumerate(candidates):
        parts.append(f"--- 知识块 #{i+1} (ID: {c['chunk_id']}) ---")
        parts.append(c["content"])
        parts.append("")
    return [
        {"role": "system", "content": RERANK_BATCH_SYSTEM},
        {"role": "user", "content": "\n".join(parts)},
    ]
